# create 1D array
arr_1d = np.array([1,2,3,4])
print(arr_1d)
print("array shape:",arr_1d.shape)
print("total element:",arr_1d.size)
print("diamention of array:",arr_1d.ndim)
# create 2D array
arr_2d = np.array([[1,2],[3,4]])
